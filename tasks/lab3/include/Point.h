#pragma once

/**
 * @file Point.h
 * @brief Определение структуры точки на плоскости
 */

/**
 * @brief Структура для представления точки в 2D пространстве
 * 
 * Точка имеет две координаты: x (горизонтальная) и y (вертикальная).
 * Это простая структура данных без методов, используется для хранения
 * координат вершин геометрических фигур.
 * 
 * Примеры использования:
 * @code
 * Point p1;              // Создает точку (0, 0)
 * Point p2(3.5, 2.1);    // Создает точку (3.5, 2.1)
 * double x = p2.x;       // Получаем координату x
 * @endcode
 */
struct Point {
    double x;  ///< Координата X (горизонтальная ось)
    double y;  ///< Координата Y (вертикальная ось)
    
    /**
     * @brief Конструктор точки с параметрами по умолчанию
     * @param x Координата X (по умолчанию 0)
     * @param y Координата Y (по умолчанию 0)
     * 
     * Список инициализации : x(x), y(y) инициализирует поля напрямую,
     * это эффективнее чем присваивание в теле конструктора.
     */
    Point(double x = 0, double y = 0) : x(x), y(y) {}
};